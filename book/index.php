            <div class="col-sm-7">
                <p class="lead">Most organizations are oblivious to, or terrified about, web accessibility.</p>
                <p>They may be aware that 20% of their customers – people with disabilities – could be clicking away from their websites or mobile apps every day, not having bought anything or found the information they wished to find. They may have heard from one of this 20%, complaining about problems they can’t reproduce, and asking for what seem like impossible fixes.</p>
                <p>Their web teams may have read the industry standard WCAG 2.0 guidelines, but found them impenetrable. Worse, their designers may claim the guidelines are a creative straightjacket that tells them everything they can’t do, but very little about why.</p>
                <p>They know they could be sued if they don’t do the right thing, but they don’t know how far they need to go to prevent that. And they don’t know if there’s anything in it for them other than risk mitigation.</p>
                <p class="lead">There is another way.</p>
                <p>In 'Including your missing 20% by embedding web and mobile accessibility' award-winning international accessibility thought-leader Professor Jonathan Hassell will take you on a journey to transform your organization to consistently achieve the creation of websites and mobile apps that are usable for all your customers, at the most efficient cost.</p>
                <p>He’ll show you how, through following a clear, strategic business-aligned framework – the BS 8878 British Standard on Web Accessibility – you can work out what your organization has to win from accessibility, how to embed the policies and processes necessary to consistently achieve that aim throughout your organization, and how to measure the return on your investment.</p>
